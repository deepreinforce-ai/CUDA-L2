void cuda_l2_h100_fp32(torch::Tensor a, torch::Tensor b,
                                 torch::Tensor b_col_major, torch::Tensor c) {
  CHECK_TORCH_TENSOR_DTYPE(a, torch::kHalf)
  CHECK_TORCH_TENSOR_DTYPE(b_col_major, torch::kHalf)
  CHECK_TORCH_TENSOR_DTYPE(c, torch::kHalf)

  const int M = a.size(0);
  const int K = a.size(1);
  const int N = b.size(1);

  CHECK_TORCH_TENSOR_SHAPE(a, M, K)
  CHECK_TORCH_TENSOR_SHAPE(b, K, N)
  CHECK_TORCH_TENSOR_SHAPE(c, M, N)

#if defined(CUTLASS_ARCH_MMA_SM90_SUPPORTED)
  auto* ptr_A = reinterpret_cast<cutlass::half_t*>(a.data_ptr());
  auto* ptr_B = reinterpret_cast<cutlass::half_t*>(b_col_major.data_ptr());
  auto* ptr_C = reinterpret_cast<cutlass::half_t*>(c.data_ptr());

  cutlass::Status status = run_gemm_impl<HgemmLargeGroup>(ptr_A, ptr_B, ptr_C, M, N, K);

  if (status != cutlass::Status::kSuccess) {
    status = run_gemm_impl<HgemmMediumGroup>(ptr_A, ptr_B, ptr_C, M, N, K);
  }

  if (status != cutlass::Status::kSuccess) {
    status = run_gemm_impl<HgemmPrimary>(ptr_A, ptr_B, ptr_C, M, N, K);
  }

  if (status != cutlass::Status::kSuccess) {
    throw std::runtime_error("CUTLASS GEMM execution failed for all configurations");
  }

  cudaError_t cuda_status = cudaDeviceSynchronize();
  if (cuda_status != cudaSuccess) {
    std::string err_msg = "CUDA synchronization failed: ";
    err_msg += cudaGetErrorString(cuda_status);
    throw std::runtime_error(err_msg);
  }

#else
  throw std::runtime_error("CUTLASS SM90 MMA not supported on this architecture");
#endif
}