template <typename ConfigType>
bool execute_gemm(torch::Tensor a, torch::Tensor b_col_major, torch::Tensor c,
                  int M, int N, int K, std::string& error_msg) {
#if defined(CUTLASS_ARCH_MMA_SM90_SUPPORTED)
  using Gemm = typename ConfigType::Gemm;
  using StrideA = typename ConfigType::StrideA;
  using StrideB = typename ConfigType::StrideB;
  using StrideC = typename ConfigType::StrideC;
  using StrideD = typename ConfigType::StrideD;
  using ElementA = typename ConfigType::ElementA;
  using ElementB = typename ConfigType::ElementB;
  using ElementC = typename ConfigType::ElementC;

  StrideA stride_A = cutlass::make_cute_packed_stride(StrideA{}, cute::make_shape(M, K, 1));
  StrideB stride_B = cute::make_stride(int64_t(K), cute::Int<1>{}, int64_t(0));
  StrideC stride_C = cutlass::make_cute_packed_stride(StrideC{}, cute::make_shape(M, N, 1));
  StrideD stride_D = cutlass::make_cute_packed_stride(StrideD{}, cute::make_shape(M, N, 1));

  auto* ptr_A = reinterpret_cast<ElementA*>(a.data_ptr());
  auto* ptr_B = reinterpret_cast<ElementB*>(b_col_major.data_ptr());
  auto* ptr_C = reinterpret_cast<ElementC*>(c.data_ptr());
  auto* ptr_D = reinterpret_cast<ElementC*>(c.data_ptr());

  int device_id = 0;
  cudaGetDevice(&device_id);
  cutlass::KernelHardwareInfo hw_info;
  hw_info.device_id = device_id;
  hw_info.sm_count = cutlass::KernelHardwareInfo::query_device_multiprocessor_count(device_id);

  typename Gemm::Arguments arguments{
      cutlass::gemm::GemmUniversalMode::kGemm,
      {M, N, K},
      {ptr_A, stride_A, ptr_B, stride_B},
      {{1.0f, 0.0f}, ptr_C, stride_C, ptr_D, stride_D},
      hw_info
  };

  Gemm gemm;

  cutlass::Status status = gemm.can_implement(arguments);
  if (status != cutlass::Status::kSuccess) {
    error_msg = "can_implement: " + std::string(cutlass::cutlassGetStatusString(status));
    return false;
  }

  size_t workspace_size = Gemm::get_workspace_size(arguments);
  cutlass::device_memory::allocation<uint8_t> workspace(workspace_size);

  status = gemm.initialize(arguments, workspace.get());
  if (status != cutlass::Status::kSuccess) {
    error_msg = "initialize: " + std::string(cutlass::cutlassGetStatusString(status));
    return false;
  }

  status = gemm.run();
  if (status != cutlass::Status::kSuccess) {
    error_msg = "run: " + std::string(cutlass::cutlassGetStatusString(status));
    return false;
  }

  cudaError_t cuda_status = cudaDeviceSynchronize();
  if (cuda_status != cudaSuccess) {
    error_msg = "sync: " + std::string(cudaGetErrorString(cuda_status));
    return false;
  }

  return true;
#else
  error_msg = "SM90 not supported";
  return false;
#endif
}

void cuda_l2_h100_fp32(torch::Tensor a, torch::Tensor b,
                                 torch::Tensor b_col_major, torch::Tensor c) {
  CHECK_TORCH_TENSOR_DTYPE(a, torch::kHalf)
  CHECK_TORCH_TENSOR_DTYPE(b, torch::kHalf)
  CHECK_TORCH_TENSOR_DTYPE(b_col_major, torch::kHalf)
  CHECK_TORCH_TENSOR_DTYPE(c, torch::kHalf)

  const int M = static_cast<int>(a.size(0));
  const int K = static_cast<int>(a.size(1));
  const int N = static_cast<int>(b.size(1));

  CHECK_TORCH_TENSOR_SHAPE(a, M, K)
  CHECK_TORCH_TENSOR_SHAPE(b, K, N)
  CHECK_TORCH_TENSOR_SHAPE(b_col_major, K, N)
  CHECK_TORCH_TENSOR_SHAPE(c, M, N)

#if defined(CUTLASS_ARCH_MMA_SM90_SUPPORTED)
  std::string error_msg;

  if (execute_gemm<ProvenBestConfig>(a, b_col_major, c, M, N, K, error_msg)) {
    return;
  }

  if (execute_gemm<SingleCTAConfig>(a, b_col_major, c, M, N, K, error_msg)) {
    return;
  }

  if (execute_gemm<Alignment8Config>(a, b_col_major, c, M, N, K, error_msg)) {
    return;
  }

  if (execute_gemm<N192Config>(a, b_col_major, c, M, N, K, error_msg)) {
    return;
  }

  if (execute_gemm<Stage3Config>(a, b_col_major, c, M, N, K, error_msg)) {
    return;
  }

  throw std::runtime_error("All GEMM configurations failed. Last error: " + error_msg);
#else
  throw std::runtime_error("CUTLASS SM90 not supported on this device");
#endif
}